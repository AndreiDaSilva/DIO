public class Operadores {   

    public static void main(String[] args) {
        
        
        String nomeCompleto = "LINGUAGEM" + " " + "JAVA";
        System.out.println(nomeCompleto);
        
        String concatenacao = "?";
        
        System.out.println(concatenacao);
        concatenacao = 1 + 1 + 1 + "1";
        System.out.println(concatenacao);
        concatenacao = 1+"1"+1+1;
        System.out.println(concatenacao);
        concatenacao = 1+"1"+1+"1";
        System.out.println(concatenacao);
        concatenacao = "1"+1+1+1;
        System.out.println(concatenacao);

        //incrementação
        int numero = 5;

        numero++;

        System.out.println(numero);
        
        
    }


    
}
